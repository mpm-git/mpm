package cn.com.mwsn.mpm.entity;


import java.util.Date;

import javax.persistence.Entity;

import cn.com.mwsn.frame.entity.IdEntity;
import cn.com.mwsn.mpm.common.Constant.ROLE;

@Entity
public class WardRoundRecord extends IdEntity{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -6663496248255042064L;
	private String name;
	private String empID;                          //医生/护士的工号
	private String role;                           //职务
	private Date startTime;   
	private Date endTime;
	private String ward;                           //房间 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public Date getStartTime() {
		return startTime;
	}
	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
	public String getWard() {
		return ward;
	}
	public void setWard(String ward) {
		this.ward = ward;
	}
	public String getEmpID() {
		return empID;
	}
	public void setEmpID(String empID) {
		this.empID = empID;
	}

	
	
}
