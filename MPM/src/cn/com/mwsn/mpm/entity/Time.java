package cn.com.mwsn.mpm.entity;

import javax.persistence.Entity;

import cn.com.mwsn.frame.entity.IdEntity;

/**
 * 
 * @author wuda
 *
 */

@Entity
public class Time extends IdEntity{

		
	/**
	 * 
	 */
	private static final long serialVersionUID = 1605269172617598763L;
	private String account;
	private String name;
	private Integer sex;
	private String password;
	private String mobile;
	private String telephone;
	private Boolean isAllowLogin;		//是否允许登录
	private String description;
	
	
	
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getSex() {
		return sex;
	}
	public void setSex(Integer sex) {
		this.sex = sex;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public Boolean getIsAllowLogin() {
		return isAllowLogin;
	}
	public void setIsAllowLogin(Boolean isAllowLogin) {
		this.isAllowLogin = isAllowLogin;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
