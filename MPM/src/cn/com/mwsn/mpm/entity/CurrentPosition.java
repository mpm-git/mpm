package cn.com.mwsn.mpm.entity;

import javax.persistence.Entity;

import cn.com.mwsn.frame.entity.IdEntity;


/**
 * 人员当前位置实体类，本地生成
 * @author zhoujie
 *
 */
@Entity
public class CurrentPosition extends IdEntity {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 3068850747747798830L;
	
	private String rfid;
	private String role;
	private String name;
	private Integer x;//current position x
	private Integer y;//current position y
	private Integer x0;//last position x
	private Integer y0;//last position y
	private Integer inpatientAreaNum;//for future usage

	public String getRfid() {
		return rfid;
	}
	public void setRfid(String rfid) {
		this.rfid = rfid;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	public Integer getX0() {
		return x0;
	}
	public void setX0(Integer x0) {
		this.x0 = x0;
	}
	public Integer getY0() {
		return y0;
	}
	public void setY0(Integer y0) {
		this.y0 = y0;
	}	
	public Integer getInpatientAreaNum() {
		return inpatientAreaNum;
	}
	public void setInpatientAreaNum(Integer inpatientAreaNum) {
		this.inpatientAreaNum = inpatientAreaNum;
	}
	
}
